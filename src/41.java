public class CollaborativeProject {
    public static void main(String[] args) {
        // Example of using loops and conditionals in Java

        // For loop example
        for (int i = 0; i < 5; i++) {
            System.out.println("Looping through the numbers from 0 to " + (i + 1));
        }

        // While loop example
        int x = 5;
        while (x > 0) {
            System.out.println(x);
            x--;
        }
    }
}
